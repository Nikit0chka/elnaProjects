bool EnterCriticalSection(int sec)
{
    int msec;
    for(msec = 0; msec < sec * 1000; msec +=10) {
      if (pthread_mutex_trylock(&g__run_lock))
        return true;
    }

    return false;
}

bool LeaveCriticalSection()
{
    pthread_mutex_unlock(&g__run_lock);
    return true;
}

